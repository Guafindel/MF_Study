package day01;

public class Lesson3_String {
	public static void main(String[] args) {
		
		System.out.println("Hellow World"); // String 문자열
		System.out.println('H'); // Character 타입, 하나의 문자를 나타낸다.
		System.out.println("H"); // 위와 같은 문자지만 위는 작은 따옴표 char, 이것은 String을 뜻함
		
		System.out.println("Hello "
				+ "World"); // 이런식으로는 줄바꿈이 불가능한 것을 알 수 있다.
		
		System.out.println("Hello \nWorld"); // \n의 의미는 new line으로 줄바꿈 기능
		System.out.println("Hello \"World\""); // Hello "World"
	}
}
