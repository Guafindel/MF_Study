package day01;

public class Lesson6_Letter {
	public static void main(String[] args) {
		String name = "JongWon";
		System.out.println("Hello," + name + "...." + name + "... bye");
		
		double VAT = 10.0; // VAT는 부가가치세
		System.out.println(VAT); 
		System.out.println(10);
		
		// 위의 두 출력은 똑같은 10이지만 보는 사람의 입장에서 위의 VAT는 부가가치세의 의미를 뜻한다
		// 하지만 밑의 10은 단순한 10일 뿐 어떠한 의미를 지닌 것은 아니다. 이것이 데이터 타입을 지정함으로서 생기는 의의다.
	}
}
